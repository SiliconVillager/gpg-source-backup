#include "CLog.h"
//--------------------------------------------------------------------------------
CLog::CLog()
{
}
//--------------------------------------------------------------------------------
CLog& CLog::Get()
{
	static CLog log;
	return( log );
}
//--------------------------------------------------------------------------------
bool CLog::Open()
{
	AppLog.open( "Log.txt" );
	Write( "Log file opened." );

	return( true );
}
//--------------------------------------------------------------------------------
bool CLog::Write( const char *cTextString )
{
	AppLog << cTextString << "\n";
	return( true );
}
//--------------------------------------------------------------------------------
bool CLog::Write( std::string& TextString )
{
	CLog::Write( TextString.c_str() );
	AppLog.flush();
	return( true );
}
//--------------------------------------------------------------------------------
bool CLog::Close( )
{
	Write( "Log file closed." );
	AppLog.close();
	return( true );
}
//--------------------------------------------------------------------------------
bool CLog::WriteSeparater( )
{
	Write( "------------------------------------------------------------" );

	return( true );
}
